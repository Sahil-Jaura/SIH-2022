function clickOnMarker(marker,str){
    const infowindow = new google.maps.InfoWindow({
      content: str,
    });
    marker.addListener("click", () => {
      infowindow.open({
        anchor: marker,
        map,
        shouldFocus: false,
      });
    });
    return marker;
  }

